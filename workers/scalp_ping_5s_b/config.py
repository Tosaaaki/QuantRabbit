def cooldown_dynamic_multiplier(
    *, spread_pips: float | None, atr_pips: float | None
) -> float:
    spread_mult = _linear_factor(
        value=spread_pips,
        pivot=COOLDOWN_SPREAD_PIVOT_PIPS,
        slope=COOLDOWN_SPREAD_SLOPE,
        factor_min=COOLDOWN_DYNAMIC_MULT_MIN,
        factor_max=COOLDOWN_DYNAMIC_MULT_MAX,
    )
    vol_mult = _linear_factor(
        value=atr_pips,
        pivot=COOLDOWN_VOL_PIVOT_ATR_PIPS,
        slope=COOLDOWN_VOL_SLOPE,
        factor_min=COOLDOWN_DYNAMIC_MULT_MIN,
        factor_max=COOLDOWN_DYNAMIC_MULT_MAX,
    )
    return _clamp(
        spread_mult * vol_mult,
        COOLDOWN_DYNAMIC_MULT_MIN,
        COOLDOWN_DYNAMIC_MULT_MAX,
    )


