def _env_bool(name: str, default: bool) -> bool:
    raw = os.getenv(name)
    if raw is None:
        return default
    return raw.strip().lower() not in {"", "0", "false", "no"}


def _env_float(name: str, default: float) -> float:
    raw = os.getenv(name)
    if raw is None:
        return default
    try:
        return float(raw)
    except ValueError:
        return default


def _clamp(value: float, low: float, high: float) -> float:
    if low > high:
        low, high = high, low
    return max(low, min(high, float(value)))


def _linear_factor(
    *,
    value: float | None,
    pivot: float,
    slope: float,
    factor_min: float,
    factor_max: float,
) -> float:
    if value is None:
        return 1.0
    span = max(abs(float(pivot)), 1e-6)
    delta = (float(value) - float(pivot)) / span
    factor = 1.0 + float(slope) * delta
    return _clamp(factor, factor_min, factor_max)


